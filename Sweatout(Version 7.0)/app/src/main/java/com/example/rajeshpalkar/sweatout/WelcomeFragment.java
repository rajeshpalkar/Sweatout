public class WelcomeFragment extends Fragment {
    // TODO: Rename parameter arguments, choose names that match
    // the fragment initialization parameters, e.g. ARG_ITEM_NUMBER
    private static final String ARG_PARAM1 = "param1";
    private static final String ARG_PARAM2 = "param2";

    // TODO: Rename and change types of parameters
    private String mParam1;
    private String mParam2;
    private Button logOut;
    private TextView welcomeName;

    private FirebaseAuth firebaseAuth;



    public WelcomeFragment() {
        // Required empty public constructor
    }

    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @param param1 Parameter 1.
     * @param param2 Parameter 2.
     * @return A new instance of fragment WelcomeFragment.
     */
    // TODO: Rename and change types and number of parameters
    public static WelcomeFragment newInstance(String param1, String param2) {
        WelcomeFragment fragment = new WelcomeFragment();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        args.putString(ARG_PARAM2, param2);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }


        firebaseAuth = FirebaseAuth.getInstance();


    }


    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View rootView = inflater.inflate(R.layout.fragment_welcome, container, false);
        welcomeName = (TextView) rootView.findViewById(R.id.welcometext);
        ImageView imgProfilePic = (ImageView) rootView.findViewById(R.id.ppimage);

        FirebaseUser user = firebaseAuth.getCurrentUser();
        String displayName = (String) user.getDisplayName();
        if(displayName!=null) {
            welcomeName.setText("Welcome,\n " + user.getDisplayName());
            String personPhotoUrl = user.getPhotoUrl().toString();
            Picasso.with(getContext()).load(personPhotoUrl).into(imgProfilePic);
        }

        else welcomeName.setText("Welcome,\n "+ user.getEmail());


        return rootView;
    }

}
